import matplotlib.pyplot as plt
import numpy as np

plt.style.use("seaborn-poster")

from scipy import signal

f = 5                                               #frecuencia = 5Hz
t = np.linspace(0, 1, 1000)                         # Desde 0 hasta 1, representando 50 puntos - vector del tiempo
y = signal.square(2 * np.pi * f * t)

plt.plot(t,y,'g')
plt.figure(figsize = (10 , 6))                      # se define el tamaño de la figura
plt.title('Square wave: s(t) = square (2πft)')
plt.xlabel("Time")
plt.ylabel("Amplitude")
plt.axhline(y=0, color='k')
plt.show()

