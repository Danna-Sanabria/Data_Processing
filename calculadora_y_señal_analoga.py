# señal analoga f(x) = A Sin(2πfT + φ)

import numpy as np
import matplotlib.pyplot as plt

plt.style.use("seaborn-poster")

A = 2                       #AMPLITUDE
f = 3                       #FRECUNCY
fi = 0                      #PHASE
t = np.linspace(0, 1.5, 1000)
y = A*(np.sin(2* np.pi * f * t + fi))

plt.figure(figsize = (8 , 8))
plt.plot(t, y, "g")
plt.title('Sine wave f(x) = A Sin(2πfT + φ)')
plt.xlabel("Time")
plt.ylabel("Amplitude")
plt.axhline(y=0, color='blue')
plt.show()